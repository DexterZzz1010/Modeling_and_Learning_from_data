def compare_strings(strings):
    n = len(strings)
    differences = []

    for i in range(n):
        for j in range(i + 1, n):
            diff_indices = [index for index, (x, y) in enumerate(zip(strings[i], strings[j])) if x != y]
            diff_values = [strings[i][index] for index in diff_indices]
            differences.append((i, j, diff_indices, diff_values))

    return differences

# 输入n个1*200的字符串，存储在一个列表中
